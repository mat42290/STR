/**
 * @brief Thread handling server communication with the monitor.
 */
void Tasks::ServerTask(void *arg) {
    int status;
    
    cout << "Start " << __PRETTY_FUNCTION__ << endl << flush;
    // Synchronization barrier (waiting that all tasks are started)
    rt_sem_p(&sem_barrier, TM_INFINITE);

    /**************************************************************************************/
    /* The task server starts here                                                        */
    /**************************************************************************************/
    rt_mutex_acquire(&mutex_monitor, TM_INFINITE);
    status = monitor.Open(SERVER_PORT);
    rt_mutex_release(&mutex_monitor);

    cout << "Open server on port " << (SERVER_PORT) << " (" << status << ")" << endl;

    if (status < 0) throw std::runtime_error {
        "Unable to start server on port " + std::to_string(SERVER_PORT)
    };
    monitor.AcceptClient(); // Wait the monitor client
    cout << "Rock'n'Roll baby, client accepted!" << endl << flush;
    rt_sem_broadcast(&sem_serverOk);
}

/**
 * @brief Thread sending data to monitor.
 */
void Tasks::SendToMonTask(void* arg) {
    Message *msg;
    
    cout << "Start " << __PRETTY_FUNCTION__ << endl << flush;
    // Synchronization barrier (waiting that all tasks are starting)
    rt_sem_p(&sem_barrier, TM_INFINITE);

    /**************************************************************************************/
    /* The task sendToMon starts here                                                     */
    /**************************************************************************************/
    rt_sem_p(&sem_serverOk, TM_INFINITE);

    while (1) {
        cout << "wait msg to send" << endl << flush;
        msg = ReadInQueue(&q_messageToMon);
        cout << "Send msg to mon: " << msg->ToString() << endl << flush;
        rt_mutex_acquire(&mutex_monitor, TM_INFINITE);
        monitor.Write(msg); // The message is deleted with the Write
        rt_mutex_release(&mutex_monitor);
    }
}

/**
 * @brief Thread restarting all other tasks
 */
void Tasks::RestartTasks(void *arg) {
    cout << "Start " << __PRETTY_FUNCTION__ << endl << flush;

    rt_task_delete(&th_sendToMon);
    rt_task_delete(&th_openComRobot);
    rt_task_delete(&th_receiveFromMon);
    rt_task_delete(&th_startRobot);
    rt_task_delete(&th_move);
    rt_task_delete(&th_batteryLevel);
    rt_task_delete(&th_watchdogReset);
    rt_task_delete(&th_startCamera);
    rt_queue_delete(&q_messageToMon);

    this->Init();
    this->Run();
    rt_sem_broadcast(&sem_barrier);
    //this->Join();

    rt_task_delete(&th_restart);
}

/**
 * @brief Thread receiving data from monitor.
 */
void Tasks::ReceiveFromMonTask(void *arg) {
    Message *msgRcv;
    int status;
    int err;
    
    cout << "Start " << __PRETTY_FUNCTION__ << endl << flush;
    // Synchronization barrier (waiting that all tasks are starting)
    rt_sem_p(&sem_barrier, TM_INFINITE);
    
    /**************************************************************************************/
    /* The task receiveFromMon starts here                                                */
    /**************************************************************************************/
    rt_sem_p(&sem_serverOk, TM_INFINITE);
    cout << "Received message from monitor activated" << endl << flush;

    while (1) {
        msgRcv = monitor.Read();
        cout << "Rcv <= " << msgRcv->ToString() << endl << flush;

        if (msgRcv->CompareID(MESSAGE_MONITOR_LOST)) {
            
            this->Stop();
            
            if (err = rt_task_create(&th_restart, "th_restart", 0, PRIORITY_TRESTART, 0)) {
                cerr << "Error task create: " << strerror(-err) << endl << flush;
                exit(EXIT_FAILURE);
            }
            
            if (err = rt_task_start(&th_restart, (void(*)(void*)) & Tasks::RestartTasks, this)) {
                cerr << "Error task start: " << strerror(-err) << endl << flush;
                exit(EXIT_FAILURE);
            }
            
            rt_task_suspend(&th_receiveFromMon);
        } else if (msgRcv->CompareID(MESSAGE_ROBOT_COM_OPEN)) {
            rt_sem_v(&sem_openComRobot);
        } else if (msgRcv->CompareID(MESSAGE_ROBOT_START_WITH_WD)) {
            rt_mutex_acquire(&mutex_withWD,TM_INFINITE);
            withWD = 1;
            rt_mutex_release(&mutex_withWD);
            rt_sem_v(&sem_startRobot);
        } else if (msgRcv->CompareID(MESSAGE_ROBOT_START_WITHOUT_WD)) {
            rt_mutex_acquire(&mutex_withWD,TM_INFINITE);
            withWD = 0;
            rt_mutex_release(&mutex_withWD);
            rt_sem_v(&sem_startRobot);
        } else if (msgRcv->CompareID(MESSAGE_CAM_OPEN)) {
            rt_sem_v(&sem_startCamera);
        } else if (msgRcv->CompareID(MESSAGE_ROBOT_GO_FORWARD) ||
                msgRcv->CompareID(MESSAGE_ROBOT_GO_BACKWARD) ||
                msgRcv->CompareID(MESSAGE_ROBOT_GO_LEFT) ||
                msgRcv->CompareID(MESSAGE_ROBOT_GO_RIGHT) ||
                msgRcv->CompareID(MESSAGE_ROBOT_STOP)) {

            rt_mutex_acquire(&mutex_move, TM_INFINITE);
            move = msgRcv->GetID();
            rt_mutex_release(&mutex_move);
        }
        delete(msgRcv); // mus be deleted manually, no consumer
    }
}

/**
 * @brief Thread opening communication with the robot.
 */
void Tasks::OpenComRobot(void *arg) {
    int status;
    int err;

    cout << "Start " << __PRETTY_FUNCTION__ << endl << flush;
    // Synchronization barrier (waiting that all tasks are starting)
    rt_sem_p(&sem_barrier, TM_INFINITE);
    
    /**************************************************************************************/
    /* The task openComRobot starts here                                                  */
    /**************************************************************************************/
    while (1) {
        rt_sem_p(&sem_openComRobot, TM_INFINITE);
        cout << "Open serial com (";
        rt_mutex_acquire(&mutex_robot, TM_INFINITE);
        status = robot.Open();
        rt_mutex_release(&mutex_robot);
        cout << status;
        cout << ")" << endl << flush;

        Message * msgSend;
        if (status < 0) {
            msgSend = new Message(MESSAGE_ANSWER_NACK);
        } else {
            msgSend = new Message(MESSAGE_ANSWER_ACK);
        }
        WriteInQueue(&q_messageToMon, msgSend); // msgSend will be deleted by sendToMon
    }
}

/**
 * @brief Thread starting the communication with the robot.
 */
void Tasks::StartRobotTask(void *arg) {
    int err;
    int wd;
    cout << "Start " << __PRETTY_FUNCTION__ << endl << flush;
    // Synchronization barrier (waiting that all tasks are starting)
    rt_sem_p(&sem_barrier, TM_INFINITE);
    
    
    /**************************************************************************************/
    /* The task startRobot starts here                                                    */
    /**************************************************************************************/
    while (1) {

        Message * msgSend;
        rt_sem_p(&sem_startRobot, TM_INFINITE);
        
        rt_mutex_acquire(&mutex_withWD, TM_INFINITE);
        wd = withWD;
        rt_mutex_release(&mutex_withWD);
        
        if(wd == 0){
            cout << "Start robot without watchdog (";
            msgSend = WriteToRobot(robot.StartWithoutWD());
        }
        else{
            cout << "Start robot with watchdog (";
            msgSend = WriteToRobot(robot.StartWithWD());
        }
        cout << msgSend->GetID();
        cout << ")" << endl;
            
        cout << "Movement answer: " << msgSend->ToString() << endl << flush;
        WriteInQueue(&q_messageToMon, msgSend);  // msgSend will be deleted by sendToMon

        if (msgSend->GetID() == MESSAGE_ANSWER_ACK) {
            rt_mutex_acquire(&mutex_robotStarted, TM_INFINITE);
            robotStarted = 1;
            rt_mutex_release(&mutex_robotStarted);
        }
    }
}

/**
 * @brief Thread handling control of the robot.
 */
void Tasks::MoveTask(void *arg) {
    int rs;
    int cpMove;
    
    cout << "Start " << __PRETTY_FUNCTION__ << endl << flush;
    // Synchronization barrier (waiting that all tasks are starting)
    rt_sem_p(&sem_barrier, TM_INFINITE);
    
    /**************************************************************************************/
    /* The task starts here                                                               */
    /**************************************************************************************/
    rt_task_set_periodic(NULL, TM_NOW, 100000000);

    while (1) {
        rt_task_wait_period(NULL);
        cout << "Periodic movement update" << endl << flush;
        rt_mutex_acquire(&mutex_robotStarted, TM_INFINITE);
        rs = robotStarted;
        rt_mutex_release(&mutex_robotStarted);
        if (rs == 1) {
            rt_mutex_acquire(&mutex_move, TM_INFINITE);
            cpMove = move;
            rt_mutex_release(&mutex_move);
            
            cout << " move: " << cpMove << endl << flush;
            
            Message* answer = WriteToRobot(new Message((MessageID)cpMove));
        }
    }
}

/**
   * @brief Thread handling battery level of the robot.
*/
void Tasks::BatteryTask(void *arg) {
    int rs;
    
    cout << "Start " << __PRETTY_FUNCTION__ << endl << flush;
    // Synchronization barrier (waiting that all tasks are starting)
    rt_sem_p(&sem_barrier, TM_INFINITE);
    
    /**************************************************************************************/
    /* The task starts here                                                               */
    /**************************************************************************************/
    rt_task_set_periodic(NULL, TM_NOW, 500000000);
    
    while(1) {
        rt_task_wait_period(NULL);
        cout << "Periodic battery update" << endl << flush;
        rt_mutex_acquire(&mutex_robotStarted, TM_INFINITE);
        rs = robotStarted;
        rt_mutex_release(&mutex_robotStarted);
        if (rs == 1) {
            Message* batteryLevel = WriteToRobot(new Message(MESSAGE_ROBOT_BATTERY_GET));
            cout << "Battery level:" << batteryLevel->ToString() << endl << flush;
            
            // On ne forward pas le message si c'est un message d'erreur sinon on perd la connection
            if(!batteryLevel->CompareID(MESSAGE_ANSWER_ROBOT_ERROR) && !batteryLevel->CompareID(MESSAGE_ANSWER_ROBOT_TIMEOUT)) {
               WriteInQueue(&q_messageToMon, batteryLevel);  // msgSend will be deleted by sendToMon
            }
        }
    }
}


void Tasks::WatchdogResetTask(void * arg){
    int rs;
    int wd;
    Message * ack;
    cout << "Start " << __PRETTY_FUNCTION__ << endl << flush;
    rt_sem_p(&sem_barrier, TM_INFINITE);
    rt_task_set_periodic(NULL, TM_NOW, 100000000);
    
    while(1) {
        
        rt_task_wait_period(NULL);
        rt_sem_p(&sem_watchdogReset, TM_INFINITE);
        cout << "Periodic watchdog reset" << endl << flush;
        
        rt_mutex_acquire(&mutex_robotStarted, TM_INFINITE);
        rs = robotStarted;
        rt_mutex_release(&mutex_robotStarted);
        
        rt_mutex_acquire(&mutex_withWD, TM_INFINITE);
        wd = withWD;
        rt_mutex_release(&mutex_withWD);
        if (rs == 1 && wd == 1) {
            ack = WriteToRobot(robot.ReloadWD());
            cout << "ACK WDRST " << ack->ToString() << endl <<flush;
        }
    }
}

void Tasks::StartCameraTask(void *arg) {
    cout << "Start " << __PRETTY_FUNCTION__ << endl << flush;
    // Synchronization barrier (waiting that all tasks are starting)
    rt_sem_p(&sem_barrier, TM_INFINITE);
    
    /**************************************************************************************/
    /* The task starts here                                                               */
    /**************************************************************************************/
    while(1) {
        rt_sem_p(&sem_startCamera, TM_INFINITE);
        rt_mutex_acquire(&mutex_camera, TM_INFINITE);
        bool cameraOpened = camera.Open();
        rt_mutex_release(&mutex_camera);
        if (cameraOpened) {
            rt_mutex_acquire(&mutex_cameraStarted, TM_INFINITE);
            cameraStarted = true;
            rt_mutex_release(&mutex_cameraStarted);
            cout << "Camera start success" << endl << flush;
        } else {
            cout << "Camera start fail" << endl << flush;
        }
    }
}
Message* Tasks::WriteToRobot(Message * msg){
    int status;
    int cf;
    int err;
    int rs;
    
    rt_mutex_acquire(&mutex_robot, TM_INFINITE);
    Message* answer = robot.Write(msg);
    cout << "Count comFailure: " << comFailure << endl << flush;
    if(answer->CompareID(MESSAGE_ANSWER_ROBOT_ERROR) ||
        answer->CompareID(MESSAGE_ANSWER_ROBOT_TIMEOUT)){

        rt_mutex_acquire(&mutex_comFailure, TM_INFINITE);
        comFailure++;
        cf = comFailure;
        rt_mutex_release(&mutex_comFailure);

        if(cf>3){
            cout << endl << endl << endl;
            cout << "More than 3 consecutives failures, restart" << endl<< flush;
            cout << endl << endl << endl << flush;

            rt_mutex_acquire(&mutex_comFailure, TM_INFINITE);
            comFailure = 0;
            rt_mutex_release(&mutex_comFailure);

            /*Message * msgSend;
            msgSend = new Message(MESSAGE_MONITOR_LOST);
            WriteInQueue(&q_messageToMon, msgSend); // msgSend will be deleted by sendToMon*/

            rt_mutex_acquire(&mutex_robotStarted, TM_INFINITE);
            robotStarted = 0;
            rt_mutex_release(&mutex_robotStarted);

            robot.Close();

            rt_sem_broadcast(&sem_startRobot);
        }
    }
    else{
        comFailure = 0;
    }
    rt_mutex_release(&mutex_robot);
    return answer;
}

/**
 * Write a message in a given queue
 * @param queue Queue identifier
 * @param msg Message to be stored
 */
void Tasks::WriteInQueue(RT_QUEUE *queue, Message *msg) {
    int err;
    if ((err = rt_queue_write(queue, (const void *) &msg, sizeof ((const void *) &msg), Q_NORMAL)) < 0) {
        cerr << "Write in queue failed: " << strerror(-err) << endl << flush;
        throw std::runtime_error{"Error in write in queue"};
    }
}

/**
 * Read a message from a given queue, block if empty
 * @param queue Queue identifier
 * @return Message read
 */
Message *Tasks::ReadInQueue(RT_QUEUE *queue) {
    int err;
    Message *msg;

    if ((err = rt_queue_read(queue, &msg, sizeof ((void*) &msg), TM_INFINITE)) < 0) {
        cout << "Read in queue failed: " << strerror(-err) << endl << flush;
        throw std::runtime_error{"Error in read in queue"};
    }/** else {
        cout << "@msg :" << msg << endl << flush;
    } /**/

    return msg;
}

